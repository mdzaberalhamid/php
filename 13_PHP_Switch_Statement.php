<?php

echo "PHP switch Statement\n";

echo "\nExample:\n";

$day = 3;

switch ($day) {
  
  case 1:
  echo "Monday\n";
  break;

  case 2:
  echo "Tuesday\n";
  break;

  case 3:
  echo "Wednesday\n";
  break;

  case 4:
  echo "Thursday\n";
  break;

  case 5:
  echo "Friday\n";
  break;

  default:
  echo "Rest Day!\n";
}

?>
